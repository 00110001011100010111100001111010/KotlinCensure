    override fun splitLine(line: String): List<String> {
        val result: MutableList<String> = mutableListOf()
        var buf = ""

        val patternPrep: Regex = getPatternPrep()

        val processedLine: String = CommonLanguagePatterns.PUNCTUATION_1.replace(line, "").let {
            CommonLanguagePatterns.PUNCTUATION_2.replace(it, "")
        }

        val words: List<String> = CommonLanguagePatterns.SPACE.split(processedLine)

        for (word: String in words)
        {
            if(word.length < 3 && !patternPrep.matches(word))
                buf += word

            else {
                if (buf.isNotEmpty()) {
                    result.add(buf)
                    buf = ""
                }

                result.add(word)
            }
        }

        if (buf.isNotEmpty())
            result.add(buf)

        return result
    }
