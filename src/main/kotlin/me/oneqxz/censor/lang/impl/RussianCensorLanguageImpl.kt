            Regex("&#121;") to "у",
            Regex("3[.,]14\\d*") to "пи",
            Regex("2[.,]72\\d*") to "е"
        )
    }

    override fun getPatternPrep(): Regex {
        return Regex("(ах?)|(в)|([вмт]ы)|(д[ао])|(же)|(за)")
