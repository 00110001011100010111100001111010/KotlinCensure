        return arrayOf(
            Regex("&[Ee][Uu][Mm][Ll];") to "е",
            Regex("&[Uu][Uu][Mm][Ll];") to "и",
            Regex("&[Aa][Uu][Mm][Ll];") to "а",
            Regex("&[Oo][Uu][Mm][Ll];") to "о",
            Regex("&[Yy][Uu][Mm][Ll];") to "у",

            Regex("&#203;") to "е",

            Regex("&[Cc][Ee][Nn][Tt];") to "с",
            Regex("&#162;") to "с",

            Regex("\\|\\\\\\|") to "и",
            Regex("/\\\\") to "л",

            Regex("><") to "х",
            Regex("><") to "х",
            Regex("\\)\\(") to "х",
            Regex("}\\{") to "х",

            Regex(">\\|<") to "ж",
            Regex("}\\|\\{") to "ж",

            Regex("/") to "y",
            Regex("-/") to "y",
            Regex("-/") to "y",

            Regex("b\\|") to "ы",
            Regex("bI") to "ы",
            Regex("bl") to "ы",

